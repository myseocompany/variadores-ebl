export function formatHorsepower(horsepower: number): string {
  return `${new Intl.NumberFormat('es-CO', { maximumFractionDigits: 2 }).format(horsepower)} HP`;
}

export function formatCurrency(price: number): string {
  return new Intl.NumberFormat('es-CO', {
    style: 'currency',
    currency: 'COP',
    maximumFractionDigits: 0,
  }).format(price);
}

export function formatVoltageRange(item: Pick<DriveInventoryItem, 'voltageMin' | 'voltageMax'>): string {
  if (item.voltageMin === item.voltageMax) {
    return `${item.voltageMin} V`;
  }

  return `${item.voltageMin}-${item.voltageMax} V`;
}

function matchesVoltage(item: DriveInventoryItem, voltage: number): boolean {
  return item.voltageMin <= voltage && voltage <= item.voltageMax;
}

function getSeriesPenalty(series: DriveSeries, preferredSeries: DriveSeries[]): number {
  const seriesIndex = preferredSeries.indexOf(series);
  return seriesIndex === -1 ? preferredSeries.length + 2 : seriesIndex;
}

function getEnclosurePenalty(enclosure: EnclosureRating, installation: InstallationEnvironment): number {
  if (installation === 'expuesto') {
    if (enclosure === 'IP66') {
      return 0;
    }

    if (enclosure === 'IP55') {
      return 1;
    }

    if (enclosure === 'IP20') {
      return 3;
    }

    return 4;
  }

  if (enclosure === 'IP20') {
    return 0;
  }

  if (enclosure === 'IP55') {
    return 1;
  }

  if (enclosure === 'IP66') {
    return 2;
  }

  return 3;
}

export function getRecommendedDrives(input: DriveSelectionInput): DriveInventoryItem[] {
  const profile = applicationProfiles[input.application];
  let candidates = driveInventory.filter(
    (item) =>
      item.supplyPhase === input.supplyPhase &&
      matchesVoltage(item, input.voltage) &&
      item.horsepower >= input.horsepower
  );

  if (input.installation === 'expuesto') {
    const sealedCandidates = candidates.filter((item) => item.enclosure === 'IP55' || item.enclosure === 'IP66');

    if (sealedCandidates.length > 0) {
      candidates = sealedCandidates;
    }
  }

  return [...candidates].sort((left, right) => {
    const seriesDelta = getSeriesPenalty(left.series, profile.preferredSeries) - getSeriesPenalty(right.series, profile.preferredSeries);

    if (seriesDelta !== 0) {
      return seriesDelta;
    }

    const horsepowerDelta = left.horsepower - right.horsepower;

    if (horsepowerDelta !== 0) {
      return horsepowerDelta;
    }

    const enclosureDelta = getEnclosurePenalty(left.enclosure, input.installation) - getEnclosurePenalty(right.enclosure, input.installation);

    if (enclosureDelta !== 0) {
      return enclosureDelta;
    }

    if (left.price !== right.price) {
      return left.price - right.price;
    }

    return left.current - right.current;
  });
}

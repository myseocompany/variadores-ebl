const SERIES_ORDER: DriveSeries[] = ['E3', 'P2', 'ECO', 'ELEVADOR'];

function splitCsvLine(line: string): string[] {
  const columns: string[] = [];
  let current = '';
  let inQuotes = false;

  for (let index = 0; index < line.length; index += 1) {
    const character = line[index];

    if (character === '"') {
      if (inQuotes && line[index + 1] === '"') {
        current += '"';
        index += 1;
        continue;
      }

      inQuotes = !inQuotes;
      continue;
    }

    if (character === ',' && !inQuotes) {
      columns.push(current.trim());
      current = '';
      continue;
    }

    current += character;
  }

  columns.push(current.trim());
  return columns;
}

function detectEnclosure(description: string): EnclosureRating {
  const enclosureMatch = description.match(/IP(20|55|66)/i);

  if (!enclosureMatch) {
    return 'SIN_DATO';
  }

  return `IP${enclosureMatch[1]}` as EnclosureRating;
}

function detectDisplay(description: string): DisplayType {
  if (/OLED/i.test(description)) {
    return 'OLED';
  }

  if (/TFT/i.test(description)) {
    return 'TFT';
  }

  if (/DISPLAY/i.test(description)) {
    return 'DISPLAY';
  }

  return 'SIN_DISPLAY';
}

function parseInventory(csv: string): DriveInventoryItem[] {
  const rows = csv.trim().split('\n');
  const dataRows = rows.slice(1);

  return dataRows
    .map((row) => {
      const columns = splitCsvLine(row);
      const [sku, seriesRaw, supplyPhaseRaw, horsepowerRaw, voltageMinRaw, voltageMaxRaw, currentRaw, ...rest] = columns;
      const priceRaw = rest.pop() ?? '0';
      const description = rest.join(',').trim();

      return {
        sku,
        series: seriesRaw as DriveSeries,
        supplyPhase: supplyPhaseRaw as SupplyPhase,
        horsepower: Number.parseFloat(horsepowerRaw),
        voltageMin: Number.parseFloat(voltageMinRaw),
        voltageMax: Number.parseFloat(voltageMaxRaw),
        current: Number.parseFloat(currentRaw),
        description,
        price: Number.parseFloat(priceRaw),
        enclosure: detectEnclosure(description),
        hasBrakeTransistor: /TRANSISTOR/i.test(description),
        hasFilter: /FILTRO/i.test(description),
        display: detectDisplay(description),
      };
    })
    .sort((left, right) => {
      const seriesDelta = SERIES_ORDER.indexOf(left.series) - SERIES_ORDER.indexOf(right.series);

      if (seriesDelta !== 0) {
        return seriesDelta;
      }

      if (left.horsepower !== right.horsepower) {
        return left.horsepower - right.horsepower;
      }

      return left.price - right.price;
    });
}

